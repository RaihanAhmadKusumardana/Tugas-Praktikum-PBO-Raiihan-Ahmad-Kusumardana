package tugas_praktikum3_no2;

public class KonversiSuhu {
    public double celciusToFarenheit(double celcius){
        return celcius * 9/5 + 32;  // Perbaikan rumus konversi Celcius ke Fahrenheit
    }

    public double celciusToReamur(double celcius) {
        return celcius * 4/5;  // Rumus konversi Celcius ke Reamur sudah benar
    }
}
